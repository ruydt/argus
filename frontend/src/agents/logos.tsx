// ---------------------------------------------------------------------------
// Agent brand marks — the real provider logos, inlined as plain self-contained
// SVG (no runtime CSS-in-JS / ThemeProvider) so they render centered and crisp
// at any size. Glyph-only marks sit on a brand-colored rounded badge; marks that
// already ship as a full colored tile (Codex) are rendered as-is.
// ---------------------------------------------------------------------------

const GLYPH_SCALE = 0.62
const GLYPH_OFFSET = (24 * (1 - GLYPH_SCALE)) / 2

type GlyphBadgeProps = LogoProps & {
  bg: string
  glyphFill?: string
  fillRule?: 'evenodd' | 'nonzero'
  children: ReactNode
}

// A brand-colored rounded badge with the provider glyph scaled + centered.
// Glyph stays aria-hidden — callers wrap the mark in a labelled element.
function GlyphBadge({ size = 18, bg, glyphFill, fillRule, children }: GlyphBadgeProps) {
  return (
    <svg
      width={size}
      height={size}
      viewBox="0 0 24 24"
      fill="none"
      xmlns="http://www.w3.org/2000/svg"
      aria-hidden="true"
    >
      <circle cx="12" cy="12" r="12" fill={bg} />
      <g
        transform={`translate(${GLYPH_OFFSET} ${GLYPH_OFFSET}) scale(${GLYPH_SCALE})`}
        fill={glyphFill}
        fillRule={fillRule}
      >
        {children}
      </g>
    </svg>
  )
}

